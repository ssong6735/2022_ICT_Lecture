package classEx;

public class Student {

	int studentID; // 학번
	String studentName; // 이름
	int grade; // 학년
	int group; // 반
	String address; // 주소
	int age; // 나이
	String gender; // 성별
	String phoneNum; // 연락처
	
	
	public int getStudentID() {
		return studentID;
	}
	public void setStudentID(int _studentID) {
		studentID = _studentID;
	}
	
	
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String _studentName) {
		studentName = _studentName;
	}
	
	
	public int getGrade() {
		return grade;
	}
	public void setGrade(int _grade) {
		grade = _grade;
	}
	
	
	public int getGroup() {
		return group;
	}
	public void setGroup(int _group) {
		group = _group;
	}
	
	
	public String getAddress() {
		return address;
	}
	public void setAddress(String _address) {
		address = _address;
	}
	
	
	public int getAge() {
		return age;
	}
	public void setAge(int _age) {
		age = _age;
	}
	
	
	public String getGender() {
		return gender;
	}
	public void setGender(String _gender) {
		gender = _gender;
	}
	
	
	public String getPhoneNum() {
		return phoneNum;
	}
	public void setPhoneNum(String _phoneNum) {
		phoneNum = _phoneNum;
	}
	
}
